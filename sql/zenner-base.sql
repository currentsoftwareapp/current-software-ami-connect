CREATE TABLE IF NOT EXISTS ZENNER_METER_BASE (
    ORG_ID VARCHAR(16777216) NOT NULL,
    CREATED_TIME TIMESTAMP_TZ(9) NOT NULL,
    METER_ID VARCHAR(16777216) NOT NULL,
    METER_TYPE VARCHAR(16777216),
    FLOW_CAPACITY VARCHAR(16777216),
    INLET_OUTLET VARCHAR(16777216),
    UNIT_OF_MEASURE VARCHAR(16777216),
    MULTIPLIER_RULE_DISPLAY VARCHAR(16777216),
    TRUNCATION_RULE_DISPLAY VARCHAR(16777216),
    TECHNOLOGY VARCHAR(16777216),
    READ_TYPE_CODE VARCHAR(16777216),
    UNIQUE (ORG_ID, METER_ID)
);

CREATE TABLE IF NOT EXISTS ZENNER_ACCOUNT_METER_BASE (
    ORG_ID VARCHAR(16777216) NOT NULL,
    CREATED_TIME TIMESTAMP_TZ(9) NOT NULL,
    ACCOUNT_ID VARCHAR(16777216),
    METER_ID VARCHAR(16777216),
    NODE_ID VARCHAR(16777216),
    LINK_DATE VARCHAR(16777216),
    EXPIRE_DATE VARCHAR(16777216),
    UNIQUE_KEY VARCHAR(16777216) NOT NULL,
    UNIQUE (ORG_ID, UNIQUE_KEY)
);

CREATE TABLE IF NOT EXISTS ZENNER_ACCOUNT_BASE (
    ORG_ID VARCHAR(16777216) NOT NULL,
    CREATED_TIME TIMESTAMP_TZ(9) NOT NULL,
    ACCOUNT_ID VARCHAR(16777216) NOT NULL,
    NAME VARCHAR(16777216),
    ADDRESS VARCHAR(16777216),
    CITY VARCHAR(16777216),
    STATE VARCHAR(16777216),
    ZIP_CODE VARCHAR(16777216),
    FIRST_READ_DATE VARCHAR(16777216),
    UTILITY_TYPE VARCHAR(16777216),
    CYCLE VARCHAR(16777216),
    ROUTE VARCHAR(16777216),
    UNIQUE (ORG_ID, ACCOUNT_ID)
);

CREATE TABLE IF NOT EXISTS ZENNER_NODE_BASE (
    ORG_ID VARCHAR(16777216) NOT NULL,
    CREATED_TIME TIMESTAMP_TZ(9) NOT NULL,
    SERIAL_NUMBER VARCHAR(16777216) NOT NULL,
    MIU_TYPE VARCHAR(16777216),
    MIU_SUBTYPE VARCHAR(16777216),
    REGISTER_TYPE VARCHAR(16777216),
    ACTIVATION_DATE VARCHAR(16777216),
    OUT_OF_SERVICE_DATE VARCHAR(16777216),
    UNIQUE (ORG_ID, SERIAL_NUMBER)
);

CREATE TABLE IF NOT EXISTS ZENNER_READ_BASE (
    ORG_ID VARCHAR(16777216) NOT NULL,
    CREATED_TIME TIMESTAMP_TZ(9) NOT NULL,
    LAST_READ_ID VARCHAR(16777216),
    ACCOUNT_ID VARCHAR(16777216),
    METER_ID VARCHAR(16777216) NOT NULL,
    SERIAL_NUMBER VARCHAR(16777216),
    FLOWTIME VARCHAR(16777216) NOT NULL,
    READING VARCHAR(16777216),
    LAST_BILL_READ VARCHAR(16777216),
    MULTIPLIER_DISPLAY VARCHAR(16777216),
    TRUNCATION_RULE_DISPLAY VARCHAR(16777216),
    UNIQUE (ORG_ID, METER_ID, FLOWTIME)
);
